package ForLoop;

public class Printmultiple 
{

	public static void main(String[] args) 
	{
		//Increment and Decrement (i++ and i--)
		//Program to print multiple lines
	
		for (int i=1;i<5;i++)
		{
			System.out.println("Sudha");
		}
	}

}


//i=1, condition = true, Sudha, i=2 --> iteration 1
//i=2, condition = true, Sudha, i=3 --> iteration 2
//i=3, condition = true, Sudha, i=4 --> iteration 3
//i=4, condition = true, Sudha, i=5 --> iteration 4
//i=5, condition = false 