package Arrays;
//declaring an array using int datatype
public class ArrayProgram0
{

	public static void main(String[] args) 
	{
				
		int [] rollno=new int[3];//Declaring the array of size 5 and you can initialize only 5 values.
		//you will get exception error when you have size 3 and initialized  5 values.
		
		rollno[0]=74;//Initializing the array at indexing position 0
		rollno[1]=74;//Initializing the array at indexing position 1
		rollno[2]=74;//Initializing the array at indexing position 2
		rollno[3]=74;//Initializing the array at indexing position 3
		rollno[4]=74;//Initializing the array at indexing position 4	
		
				
	}

}
